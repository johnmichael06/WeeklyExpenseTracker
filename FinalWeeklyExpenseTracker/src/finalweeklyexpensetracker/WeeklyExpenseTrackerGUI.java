    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
int selectedRow = expenseTable.getSelectedRow();
        if (selectedRow != -1) {
            // Get selected row values and set them into input fields
            expenseNameField.setText(tableModel.getValueAt(selectedRow, 0).toString());
            amountField.setText(tableModel.getValueAt(selectedRow, 1).toString());
            categoryComboBox.setSelectedItem(tableModel.getValueAt(selectedRow, 2).toString());
            dayComboBox.setSelectedItem(tableModel.getValueAt(selectedRow, 3).toString());

            // Remove the old row
            tableModel.removeRow(selectedRow);

            // Add the updated row back to the table
            tableModel.addRow(new Object[]{
                expenseNameField.getText(),
                amountField.getText(),
                categoryComboBox.getSelectedItem(),
                dayComboBox.getSelectedItem()
            });

            // No need to call updateTotalSpending here anymore
        }

    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
 int selectedRow = expenseTable.getSelectedRow();
        if (selectedRow != -1) {
            // Ask for confirmation before deleting
            int confirm = JOptionPane.showConfirmDialog(null,
                    "Are you sure you want to delete this expense?",
                    "Delete Confirmation", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                // Remove the selected row from the table
                tableModel.removeRow(selectedRow);

                // No need to call updateTotalSpending here either
            }
        }
    

    }//GEN-LAST:event_deleteButtonActionPerformed

    private void viewTotalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewTotalButtonActionPerformed
  double total = 0;
    
    // Get the selected filter
    String selectedFilter = filterComboBox.getSelectedItem().toString();
    
    // Get the model and create a TableRowSorter
    DefaultTableModel model = (DefaultTableModel) expenseTable.getModel();
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    expenseTable.setRowSorter(sorter);
    
    // Apply filter based on selected filter
    if (selectedFilter.equals("Whole Week")) {
        // No filter, show all rows
        sorter.setRowFilter(null);
    } else {
        // Filter by the selected day (e.g., Monday, Tuesday)
        sorter.setRowFilter(RowFilter.regexFilter(selectedFilter));
    }
    
    // Loop through only the visible rows (after sorting/filtering)
    int rowCount = sorter.getViewRowCount();
    for (int i = 0; i < rowCount; i++) {
        // Get the model row index of the filtered row
        int modelRow = sorter.convertRowIndexToModel(i);
        
        try {
            // Get the amount in the second column (index 1) of the model
            double amount = Double.parseDouble(model.getValueAt(modelRow, 1).toString());
            total += amount;
        } catch (NumberFormatException e) {
            // Skip this row if the amount is not a valid number
            continue;
        }
    }
    
    // Update the total field to show the calculated total
    totalDisplayField.setText(String.format("%.2f", total));
    }//GEN-LAST:event_viewTotalButtonActionPerformed

