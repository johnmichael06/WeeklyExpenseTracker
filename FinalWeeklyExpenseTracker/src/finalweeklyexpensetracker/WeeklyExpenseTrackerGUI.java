    private void filterComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterComboBoxActionPerformed
 // Get the selected day or "Whole Week" from the filter dropdown
    String selectedFilter = filterComboBox.getSelectedItem().toString();

    // Set up a table sorter
    DefaultTableModel model = (DefaultTableModel) expenseTable.getModel();
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    expenseTable.setRowSorter(sorter);

    if (selectedFilter.equals("Whole Week")) {
        // Show all rows (Monday to Sunday)
        sorter.setRowFilter(null); // No filter, show all rows

        // Call the method to calculate the total amount for the whole week
        viewTotalButtonActionPerformed(evt); // This will update the total for the whole week
    } else if (!selectedFilter.equals("All")) {
        // Filter by specific day (Monday-Sunday)
        sorter.setRowFilter(RowFilter.regexFilter(selectedFilter));
    } else {
        // Show all rows (if all is selected)
        sorter.setRowFilter(null);
    }

    }//GEN-LAST:event_filterComboBoxActionPerformed

